public class EstruturaCondicionalIfElse {
    public static void main(String[] args) {
        double media = 7.5;
        if (media >= 7.0) {
            System.out.println("Aprovado");
        } else if (media >= 5.0) {
            System.out.println("Recuperação");
        } else {
            System.out.println("Reprovado");
        }
    }

}
